func upsampleImage(dst []byte, dstw int, dsth int, src []byte, srcw int, srch int) {
	if (dstw <= 0) || (dsth <= 0) || (srcw <= 0) || (srch <= 0) {
		return
	}

	// The dsty sequence is 0, 1, 3, 5, 7, 9, ...
	for dsty := 0; dsty < dsth; {
		dsty0 := dsty + 0
		dsty1 := dsty + 1
		srcy0 := dsty >> 1
		srcy1 := srcy0 + 1

		if dsty == 0 { // First row.
			dsty1 = -1
			srcy1 = 0
			dsty = 1

		} else if (dsty + 1) >= dsth { // Last row, when dsth is even.
			dsty1 = -1
			srcy1 = srcy0
			dsty += 1

		} else { // All other rows.
			dsty += 2
		}

		dst0 := dst[(dsty0+0)*dstw : (dsty0+1)*dstw]
		dst1 := []byte(nil)
		if dsty1 >= 0 {
			dst1 = dst[(dsty1+0)*dstw : (dsty1+1)*dstw]
		}
		src0 := src[(srcy0+0)*srcw : (srcy0+1)*srcw]
		src1 := src[(srcy1+0)*srcw : (srcy1+1)*srcw]
		upsampleStrip(dst0, dst1, src0, src1)
	}
}

func upsampleStrip(dst0 []byte, dst1 []byte, src0 []byte, src1 []byte) {
	if (len(dst0) <= 0) ||
		((len(dst0) != len(dst1)) && (len(dst1) != 0)) ||
		((len(src0) != len(src1)) && (len(src1) != 0)) {
		return
	}

	// t=top, l=left, c=current.
	tl := uint32(src0[0])
	cl := uint32(src1[0])

	{
		dst0[0] = uint8(((3 * tl) + cl + 2) >> 2)
	}
	if len(dst1) > 0 {
		dst1[0] = uint8(((3 * cl) + tl + 2) >> 2)
	}

	for x, lastPixelPair := 1, (len(dst0)-1)>>1; x <= lastPixelPair; x++ {
		tc := uint32(src0[x])
		cc := uint32(src1[x])

		avg := tl + tc + cl + cc + 8
		diag12 := (avg + (2 * (tc + cl))) >> 3
		diag03 := (avg + (2 * (tl + cc))) >> 3

		{
			dst0[(2*x)-1] = uint8((diag12 + tl) >> 1)
			dst0[(2*x)-0] = uint8((diag03 + tc) >> 1)
		}
		if len(dst1) > 0 {
			dst1[(2*x)-1] = uint8((diag03 + cl) >> 1)
			dst1[(2*x)-0] = uint8((diag12 + cc) >> 1)
		}

		tl = tc
		cl = cc
	}

	if (len(dst0) & 1) == 0 {
		n := len(dst0) - 1
		{
			dst0[n] = uint8(((3 * tl) + cl + 2) >> 2)
		}
		if len(dst1) > 0 {
			dst1[n] = uint8(((3 * cl) + tl + 2) >> 2)
		}
	}
}

func compare(have []byte, want []byte, width int, name string) {
	if len(have) != len(want) {
		panic("invalid compare")
	} else if bytes.Equal(have, want) {
		return
	}
	failed = true

	fmt.Fprintf(os.Stderr, "Wrong output for %s\n", name)
	for i := range have {
		fmt.Fprintf(os.Stderr, " %02X", have[i]-want[i])
		if (i % width) == (width - 1) {
			fmt.Fprintln(os.Stderr)
		}
	}
}

