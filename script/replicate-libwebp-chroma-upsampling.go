// replicate-libwebp-chroma-upsampling.go tests a Go port of libwebp's "fancy"
// 4:2:0 chroma upsampling algorithm. This is a 9:3:3:1 triangle filter (the
// 2-Dimensional analogue of the 1-Dimensional 3:1 triangle filter).
//
// libwebp's "fancy" upsampling is very similar to (but not exactly the same
// as) libjpeg's "fancy" upsampling. Both are 9:3:3:1 and the weights sum to
// 16, but they use a different integer arithmetic rounding algorithm:
//
//   - libwebp uses truncate((weighted_average + 8) / 16), everywhere.
//   - libjpeg uses truncate((weighted_average + H) / 16), where H is 8
//     (rounding up) for even columns and 7 (rounding down) for odd columns.
//
// libwebp's plain C implementation is the src/dsp/upsampling.c UPSAMPLE_FUNC
// macro. While it doesn't affect rounding, the macro also computes Cb and Cr
// simultaneously using SWAR (SIMD Within A Register), packing u16x2 into a
// u32, which this Go re-implementation does not replicate. libwebp also has
// other SIMD-optimized implementations.
//
// libjpeg's plain C implementation is the jdsample.c h2v2_fancy_upsample
// function. libjpeg also has other SIMD-optimized implementations.
//
// Usage: "go run replicate-libwebp-chroma-upsampling.go" should print nothing,
// but it would print a diff if the hard-coded tests fail.

